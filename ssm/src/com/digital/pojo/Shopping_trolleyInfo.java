package com.digital.pojo;

public class Shopping_trolleyInfo {

	private int sid;
	private float total;
	private int foodSum;
	private float shipping_fee;
	private int uid;
	private int rid;
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public float getTotal() {
		return total;
	}
	public void setTotal(float total) {
		this.total = total;
	}
	public int getFoodSum() {
		return foodSum;
	}
	public void setFoodSum(int foodSum) {
		this.foodSum = foodSum;
	}
	public float getShipping_fee() {
		return shipping_fee;
	}
	public void setShipping_fee(float shipping_fee) {
		this.shipping_fee = shipping_fee;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	
		
}
